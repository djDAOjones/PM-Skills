#!/usr/bin/env node
// @ts-check

/**
 * gen-file-map.mjs — emit / refresh the project-memory file-map skeleton.
 *
 * A pm-skills scaffold file: copied into your project root once, then
 * yours to own, extend, or delete. Requires Node (>= 18) and git only —
 * no npm install and no dependencies.
 *
 * Why this exists: `file-map.md` is the single biggest hot-read
 * line-item and the highest-maintenance memory file — it maps every
 * source file, so its *skeleton* (the path list, grouped by directory)
 * is pure mechanical bookkeeping the agent should not hand-maintain.
 * This script owns the skeleton so the agent only writes the judgement:
 * the one-line role after each path. It mirrors the decision-log's
 * headings-first pattern — a compact section index at the top lets a
 * reader (human or agent) prove nothing relevant was skipped before a
 * sectional read.
 *
 * What it does:
 * - Groups the source tree by top-level directory into `## <dir>`
 *   sections (root-level files go under `## (root)`), sorted stably so
 *   diffs stay clean and sync-conflict copies stay reviewable.
 * - Emits `` - `path` — <ROLE> `` lines. Role text is MERGED BY PATH
 *   from the existing map: an existing role is preserved verbatim; a new
 *   file gets `— (role needed)` for the agent to fill in.
 * - Never silently drops a path: a file in the old map but no longer on
 *   disk is listed under a clearly-marked "no longer on disk" block for
 *   the agent to confirm and remove.
 * - Writes a `<!-- file-map-index -->` block at the top: one line per
 *   section (name + file count) plus a total, so the sectional hot read
 *   is cheap and the BUDGET-SCALE budget can read the file count here.
 *
 * Idempotent: running it twice with no tree change produces no diff.
 *
 * File discovery uses `git ls-files` (tracked + new non-ignored files),
 * so `.gitignore` is honoured for free and there is no recursive
 * filesystem walk (which stalls on cloud-synced / on-demand checkouts).
 * The project must therefore be a git repo — the same assumption
 * check-links.mjs already makes.
 *
 * Usage:
 *   node gen-file-map.mjs                 # merge-write the default map
 *   node gen-file-map.mjs --stdout        # print the merged map, write nothing
 *   node gen-file-map.mjs path/to/map.md  # target a different map file
 *
 * Exit code: 0 on success; 1 on error (e.g. not a git repo).
 */

import { execSync } from 'node:child_process';
import { existsSync, readFileSync, writeFileSync } from 'node:fs';

/** Default target: the project-memory file-map. */
const DEFAULT_MAP = 'pm_skills/project/file-map.md';

/** Heading used for files that sit at the repository root. */
const ROOT_SECTION = '(root)';

/**
 * Path patterns excluded from the map even when git tracks them. Tune
 * this list for your project — it is the documented "ignore knob".
 * `.gitignore` already excludes build output, deps, and scratch; these
 * are extra files that are tracked but not worth a role line. The map is
 * for *source* files an agent needs to reason about.
 * @type {RegExp[]}
 */
const IGNORE = [
  /^pm_skills\//, // framework files: classed in MANIFEST.md, not source
  /(^|\/)\.[^/]+$/, // dotfiles at any level (.gitignore, .editorconfig, …)
  /(^|\/)(package-lock|pnpm-lock|yarn)\.(json|lock|ya?ml)$/,
  /(^|\/)LICENSE$/,
];

/**
 * The header preserved at the top of the map (everything before the
 * first `## ` section). Falls back to a minimal template when the file
 * does not exist yet. The generated index block is spliced in after it.
 */
const DEFAULT_HEADER = `# File Map

<!-- Skeleton generated by scaffold/gen-file-map.mjs: paths grouped by
top-level directory, sorted. Re-run after adds/renames/deletes; it
preserves role text by path and only new files need a role written.
Hot read is sectional — see AGENTS.md "Before every task". -->
`;

/** Marks the start/end of the generated section-index block. */
const INDEX_START = '<!-- file-map-index -->';
const INDEX_END = '<!-- /file-map-index -->';

/** Heading for paths in the old map that no longer exist on disk. */
const STALE_HEADING = '## No longer on disk (confirm and remove)';

/**
 * List the source files to map: everything git tracks plus new,
 * not-yet-committed, non-ignored files — never anything gitignored.
 * @returns {string[]} repo-relative paths, IGNORE-filtered, sorted
 */
function sourceFiles() {
  const tracked = execSync('git ls-files', { encoding: 'utf8' });
  const untracked = execSync('git ls-files --others --exclude-standard', {
    encoding: 'utf8',
  });
  const all = `${tracked}\n${untracked}`
    .split('\n')
    .map((s) => s.trim())
    .filter(Boolean);
  return [...new Set(all)]
    .filter((f) => existsSync(f)) // staged deletions linger in ls-files
    .filter((f) => !IGNORE.some((re) => re.test(f)))
    .sort();
}

/**
 * Parse an existing map into a path -> role-text lookup. A role line
 * looks like `` - `path` — role `` (any dash variant); the role is
 * everything after the first dash following the closing backtick.
 * @param {string} content the existing map file, or '' if none
 * @returns {Map<string, string>} path -> role text (may be empty)
 */
function existingRoles(content) {
  /** @type {Map<string, string>} */
  const roles = new Map();
  const lineRe = /^- `([^`]+)`(?:\s*[—–-]\s*(.*))?$/;
  for (const line of content.split('\n')) {
    const m = line.trim().match(lineRe);
    if (m) roles.set(m[1], (m[2] ?? '').trim());
  }
  return roles;
}

/**
 * Top-level directory of a repo-relative path, or ROOT_SECTION when the
 * file sits directly at the repo root.
 * @param {string} file
 * @returns {string}
 */
function sectionOf(file) {
  const slash = file.indexOf('/');
  return slash === -1 ? ROOT_SECTION : file.slice(0, slash);
}

/**
 * Group files by their top-level section, preserving the sorted order
 * within each section.
 * @param {string[]} files
 * @returns {Map<string, string[]>} section -> files (insertion = sorted)
 */
function groupBySection(files) {
  /** @type {Map<string, string[]>} */
  const groups = new Map();
  for (const file of files) {
    const section = sectionOf(file);
    const bucket = groups.get(section);
    if (bucket) bucket.push(file);
    else groups.set(section, [file]);
  }
  // Root first (if present), then the rest alphabetically, for stability.
  return new Map(
    [...groups.entries()].sort(([a], [b]) => {
      if (a === ROOT_SECTION) return -1;
      if (b === ROOT_SECTION) return 1;
      return a < b ? -1 : a > b ? 1 : 0;
    }),
  );
}

/**
 * Everything in the current file before the first `## ` heading, with
 * any prior generated index block stripped (it is re-emitted). Falls
 * back to DEFAULT_HEADER for a new or index-only file.
 * @param {string} content
 * @returns {string} the preserved header, trailing newline trimmed
 */
function preservedHeader(content) {
  if (!content.trim()) return DEFAULT_HEADER.trimEnd();
  const firstHeading = content.search(/^## /m);
  let header = firstHeading === -1 ? content : content.slice(0, firstHeading);
  const idxStart = header.indexOf(INDEX_START);
  if (idxStart !== -1) {
    const idxEnd = header.indexOf(INDEX_END);
    const after = idxEnd === -1 ? '' : header.slice(idxEnd + INDEX_END.length);
    header = header.slice(0, idxStart) + after;
  }
  return header.replace(/\n{3,}/g, '\n\n').trimEnd();
}

/**
 * Render the whole map: header, index block, one section per group, and
 * a trailing stale-paths block when the old map named paths now gone.
 * @param {string} header preserved header text
 * @param {Map<string, string[]>} groups section -> files
 * @param {Map<string, string>} roles path -> preserved role text
 * @param {string[]} stale paths in the old map no longer on disk
 * @returns {string}
 */
function render(header, groups, roles, stale) {
  const total = [...groups.values()].reduce((n, f) => n + f.length, 0);
  const index = [
    INDEX_START,
    `<!-- ${total} file(s) across ${groups.size} section(s); regenerate with scaffold/gen-file-map.mjs -->`,
    ...[...groups.entries()].map(([s, f]) => `- \`${s}\` — ${f.length} file(s)`),
    INDEX_END,
  ].join('\n');

  const sections = [...groups.entries()].map(([section, files]) => {
    const lines = files.map((file) => {
      const role = roles.get(file);
      return `- \`${file}\` — ${role ? role : '(role needed)'}`;
    });
    return `## ${section}\n\n${lines.join('\n')}`;
  });

  const parts = [header, index, ...sections];
  if (stale.length) {
    const staleLines = stale.map((p) => `- \`${p}\``);
    parts.push(`${STALE_HEADING}\n\n${staleLines.join('\n')}`);
  }
  return `${parts.join('\n\n')}\n`;
}

/** Parse argv into { target, stdout }. */
function parseArgs() {
  const args = process.argv.slice(2);
  const stdout = args.includes('--stdout');
  const target = args.find((a) => !a.startsWith('--')) ?? DEFAULT_MAP;
  return { target, stdout };
}

function main() {
  const { target, stdout } = parseArgs();
  const files = sourceFiles();
  const existing = existsSync(target) ? readFileSync(target, 'utf8') : '';
  const roles = existingRoles(existing);
  const groups = groupBySection(files);
  const onDisk = new Set(files);
  const stale = [...roles.keys()].filter((p) => !onDisk.has(p)).sort();
  const output = render(preservedHeader(existing), groups, roles, stale);

  if (stdout) {
    process.stdout.write(output);
    return;
  }

  writeFileSync(target, output);
  const added = files.filter((f) => !roles.has(f)).length;
  console.log(
    `gen-file-map: ${files.length} file(s), ${groups.size} section(s) -> ${target}`,
  );
  console.log(
    `  ${added} new (role needed), ${stale.length} stale path(s) flagged`,
  );
}

main();
